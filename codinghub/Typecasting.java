class Typecasting
{
public static void main(String args[])
{
int b1=1,b2=234,b3=7890;
System.out.println("Examples of automatic type casting:");
System.out.println(b1);
System.out.println(b2);
System.out.println(b3);
int b4=(int)(9.11);
System.out.println("Examples of Manual type casting:");
System.out.println(b4);
}
}