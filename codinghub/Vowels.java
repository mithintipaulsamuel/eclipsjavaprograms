class Vowels
 {
   public static void main(String args[])
   {
     char ch='o';
     if(ch=='a'||ch=='e'||ch=='i'||ch=='o'||ch=='u'||ch=='A'||ch=='E'||ch=='I'||ch=='O'||ch=='U')
      {
        System.out.println(ch+" is an vowel.");
      }
     else
     {
       System.out.println(ch+" is not an vowel.");
     }
   }
 }