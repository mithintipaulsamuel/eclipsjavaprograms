class Interchange3
 {
   public static void main(String args[])
    {
      int a=89;
      System.out.println("initial value of A : "+a);
      int b=45;
      System.out.println("Initial value of B : "+b);
      int c;
      c=a;
      a=b;
      b=c;
      System.out.println("New value of A : "+a);
      System.out.println("New value of B: "+b);
      
    }
 }