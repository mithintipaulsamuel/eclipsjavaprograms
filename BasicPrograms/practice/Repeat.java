class Repeat
 {
   public static void main(String args[])
   {
     String s1="Coding Hub";
     String s2="Java";
     String s3="Programming";
     for(int i=0;i<=9;i++)
      {
        System.out.print(s1);
      }
     for(int i=0;i<=99;i++)
      {
        System.out.println(s2);
        System.out.println(s3);
      }
   }
 }